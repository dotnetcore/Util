namespace Util.Generators {
    /// <summary>
    /// 命名约定
    /// </summary>
    public enum NamingConvention {
        /// <summary>
        /// 帕斯卡命名,所有单词首字母大写
        /// </summary>
        PascalCase,
        /// <summary>
        /// 驼峰命名,第一个单词首字母小写,其余单词首字母大写
        /// </summary>
        CamelCase
    }
}
