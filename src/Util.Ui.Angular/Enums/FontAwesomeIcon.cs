    /// <summary>
    /// FontAwesome图标扩展
    /// </summary>
    public static class FontAwesomeIconExtensions {
        /// <summary>
        /// 获取图标
        /// </summary>
        public static string GetIcon( this FontAwesomeIcon icon ) {
            return $"fa {icon.Description()}";
        }

        /// <summary>
        /// 获取图标
        /// </summary>
        public static string GetIcon( this FontAwesomeIcon? icon ) {
            return icon == null ? string.Empty : icon.Value.GetIcon();
        }
    }
